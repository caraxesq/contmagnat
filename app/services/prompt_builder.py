from app.core.constants import TOPIC_PAIN_POINTS


def build_generation_prompt(
    *,
    topic: str,
    user_request: str,
    style_examples: list[str],
) -> str:
    examples_block = _format_examples(style_examples)
    
    pains = TOPIC_PAIN_POINTS.get(topic.lower())
    if pains:
        pains_list_str = "\n".join(f"- {p}" for p in pains)
        pains_block = f"Целевые боли аудитории:\n{pains_list_str}"
    else:
        pains_block = "Определи актуальные боли целевой аудитории самостоятельно."

    return f"""Ты пишешь Telegram-пост. Голос — живой эксперт с характером: говорит прямо, иногда с иронией, никогда не занудствует.

━━━━━━━━━━━━━━━━━━━━━━
ЗАДАЧА
━━━━━━━━━━━━━━━━━━━━━━
Тематика: {topic}
Запрос: {user_request}
{pains_block}

━━━━━━━━━━━━━━━━━━━━━━
ШАГ 1 — КРЮЧОК ПО ИСВП (пиши первым, это самое важное)
━━━━━━━━━━━━━━━━━━━━━━
Крючок — это первые 1–2 строки. Они решают всё: читают пост или листают дальше.

Крючок строится СТРОГО по методу ИСВП. Возьми минимум 2 элемента из четырёх:

И — Интрига (ломает ожидание, вызывает "подожди, что?")
Примеры формул:
"[Привычное действие] — а на самом деле [неожиданное последствие]"
"Врачи не говорят об этом, потому что..."
"Ошибка которую делают даже [авторитетная группа]"
"[Объект] хотел бы с тобой поговорить" — с иронией

С — Срочность (читать нужно прямо сейчас)
Примеры формул:
"К [возраст/срок] у большинства уже..."
"Буквально вчера узнал что..."
"Это работает только пока не стало поздно"

В — Выгода (конкретный результат для читателя)
Примеры формул:
"[Конкретное действие] — и [конкретный результат]"
"Один анализ который скажет больше чем..."
Выгода должна быть ощутимой, не абстрактной

П — Причастность (это про тебя лично)
Примеры формул:
"Если ты [конкретная привычка читателя]..."
"Узнаёшь себя?"
"Для тех кто [конкретная ситуация]"
"[Число]% людей делают это каждый день — и не догадываются"

СИЛЬНЫЕ ПРИМЕРЫ КРЮЧКОВ (И+П+юмор):
"Пьёшь ибупрофен при каждой головной боли? Твои почки давно хотели с тобой поговорить." 

(С+П):
"К 45 годам каждый третий живёт с повреждёнными почками — и списывает это на обычную усталость."

(И+В+юмор):
"Один пакет чипсов — это дневная норма соли для почек. И это ещё без борща."

СЛАБЫЕ КРЮЧКИ — ЗАПРЕЩЕНО:
"180 литров крови в день..." — цифра без зацепки, читателю непонятно зачем это ему
"Три привычки которые..." — скучный анонс содержания
"Твои почки молчат до последнего..." — абстракция без личной зацепки
"X% людей не знают..." — самый заезженный шаблон ИИ-постов, никогда

━━━━━━━━━━━━━━━━━━━━━━
ШАГ 2 — ТЕЛО ПОСТА
━━━━━━━━━━━━━━━━━━━━━━
Порядок: конкретная история или случай из практики → факты и инсайты → практика

КОНКРЕТИКА — это главное отличие живого текста от нейросетевого:
Не "многие сталкиваются с этим" — а "мой пациент три года списывал это на усталость"
Не "исследования показывают" — а "в 2019 году проверили 40 000 человек — вот что вышло"
Не "нарушение водно-солевого баланса" — а "тело начинает удерживать воду там где не надо"
Не просто "180 литров" — а "180 литров — это ванна с горкой, каждый день, без выходных"

АВТОРСКАЯ ПОЗИЦИЯ:
В посте должен быть момент где автор с чем-то не согласен, чему-то удивлён или что-то считает абсурдным. Нейросеть излагает факты. Живой человек имеет отношение к фактам.

ЮМОР (одна фраза на пост, не больше):
Встраивай в крючок или в тело — никогда в финал.
Работает узнаваемое преувеличение: "почки уже написали заявление об увольнении"
Работает неожиданное сравнение: "и это ещё без борща"
Не работает: анекдоты, каламбуры, натянутые шутки

РИТМ ПРЕДЛОЖЕНИЙ:
Длинное предложение с деталью — короткое. Удар. Снова длинное. Потом ещё одно короткое.
Три коротких подряд — запрещено. Связывай причастными оборотами, противопоставлениями, уточнениями через тире.

ТЕРМИНЫ:
Каждый термин сразу расшифровывается: "креатинин — вещество по которому видно справляются ли почки с нагрузкой"
Проверка: понял бы это смышлёный 12-летний без гугла?

ТОЛЬКО РУССКИЙ ЯЗЫК:
Ни одного английского слова. Ни одной аббревиатуры без расшифровки. Абсолютный запрет.

━━━━━━━━━━━━━━━━━━━━━━
ШАГ 3 — ФИНАЛ
━━━━━━━━━━━━━━━━━━━━━━
Финал — не вывод и не мораль. Это удар который остаётся в голове.

Варианты:
– Одна фраза которая переворачивает смысл всего текста
– Вопрос с неожиданным углом (не "а как вы думаете?")
– Личное наблюдение которое читатель узнаёт в себе

CTA если нужен:
"Сохрани — пригодится" работает если контент реально ценный
"Перешли тому кто..." работает если есть конкретный адресат
НИКОГДА: "пиши в комменты", "ставь лайк", "подпишись", "поставь 🔔"

━━━━━━━━━━━━━━━━━━━━━━
ЭТАЛОННЫЙ ПОСТ
━━━━━━━━━━━━━━━━━━━━━━
Изучи структуру — твой пост должен быть устроен так же:

💊 Пьёшь ибупрофен при каждой головной боли? Твои почки давно хотели с тобой поговорить.

Ибупрофен, диклофенак, кеторол — все они сужают сосуды внутри почки. Один раз ничего страшного, но если пить их раз в неделю годами, фильтрующая ткань замещается рубцовой — той, которая уже не восстанавливается.

🔬 В нефрологии это называется анальгетическая нефропатия — повреждение почек обезболивающими. Встречается у каждого пятого пациента с хронической болезнью почек, и почти все узнают об этом случайно, сдав анализ совсем по другому поводу.

Типичная история: человек приходит с давлением, а уходит с диагнозом по почкам.

🩺 Что делать если голова болит часто:
– сначала выяснить причину, а не глушить симптом
– если без таблетки не обойтись — парацетамол щадит почки значительно мягче
– раз в год сдавать креатинин крови: по нему видно справляются ли почки с нагрузкой

Боль — это сигнал. Глушить его годами таблетками — значит платить почками.

━━━━━━━━━━━━━━━━━━━━━━
РАЗБОР ЭТАЛОНА
━━━━━━━━━━━━━━━━━━━━━━
Крючок: П ("пьёшь" — обращение к конкретной привычке) + И ("хотели поговорить" — неожиданный угол) + юмор
Эмодзи: открывают три смысловых блока, не висят для украшения
Конкретика: названия таблеток, механизм действия, название болезни с расшифровкой
Живой момент: "типичная история" — случай из практики
Один список с коротким тире (–) и вводным предложением перед ним
Финал: одна фраза которая переворачивает смысл — без морали и без призыва лайкать

━━━━━━━━━━━━━━━━━━━━━━
ПРИМЕРЫ СТИЛЯ КАНАЛА
━━━━━━━━━━━━━━━━━━━━━━
{examples_block}

━━━━━━━━━━━━━━━━━━━━━━
ТЕХНИЧЕСКИЕ ПРАВИЛА
━━━━━━━━━━━━━━━━━━━━━━
Длина: 900–1100 символов с пробелами. Посчитай после написания — если не попал, перепиши.
Списков: максимум один, только если элементов 4+, иначе впиши в текст
Тире в списках: ТОЛЬКО короткое (–). Символы ▸ • ► — ЗАПРЕЩЕНЫ в списках.
Эмодзи: 2–4 штуки, открывают смысловые блоки

━━━━━━━━━━━━━━━━━━━━━━
ЗАПРЕЩЁННЫЕ СЛОВА И ПРИЁМЫ
━━━━━━━━━━━━━━━━━━━━━━
"X% людей не знают" / "в наше время" / "как известно" / "многие из нас" / "не секрет что" / "И вот ещё что" / "Кстати говоря" / "Но есть нюанс"
Два списка в одном посте
Три коротких предложения подряд
Финал с абстрактной метафорой ("как маяк", "как тихий друг", "как фильтр жизни")
Любое английское слово или аббревиатура без расшифровки
Символы ▸ • ► в списках — ЗАПРЕЩЕНЫ, только короткое тире (–)
"Поставь 🔔" / "ставь лайк" / "подпишись" / "пиши в комменты"

━━━━━━━━━━━━━━━━━━━━━━
РЕЗУЛЬТАТ
━━━━━━━━━━━━━━━━━━━━━━
Только готовый пост. Без комментариев, пояснений, заголовков разделов.

Перед выдачей проверь:
1. Крючок содержит минимум 2 элемента ИСВП?
2. Есть конкретная деталь которую нельзя выдумать — только знать?
3. Длина 900–1100 символов?
4. Нет английских слов и нераскрытых аббревиатур?
5. Есть авторская позиция — момент где автор с чем-то не согласен или чему-то удивлён?

Если хотя бы один пункт не выполнен — перепиши."""


def build_formatting_prompt(post_text: str) -> str:
    return f"""Твоя задача — расставить форматирование Telegram в готовом посте. Текст не меняй — только добавь теги.

СИНТАКСИС TELEGRAM:
**жирный** — двойные звёздочки
*курсив* — одинарные звёздочки
||спойлер|| — двойные вертикальные черты

ЧТО ВЫДЕЛЯТЬ:

**Жирным (строго 2–3 раза, не больше):**
– Главная мысль поста — одна фраза ради которой всё написано
– Ключевая цифра или факт который удивляет
– Финальный удар — последняя сильная фраза

*Курсивом (1–2 раза):*
– Живой момент или наблюдение из практики
– Ироничная фраза или внутренний голос читателя
– Уточнение в сторону

||Спойлером (не обязательно, по ситуации):||
– Финальный вопрос или неожиданная мысль которую интересно "открыть"

ПРИМЕР:

Было:
Ибупрофен сужает сосуды внутри почки. Один раз — ничего страшного. Но если пить раз в неделю годами, фильтрующая ткань замещается рубцовой — той которая не восстанавливается. Типичная история: человек приходит с давлением, уходит с диагнозом по почкам. Боль — это сигнал. Глушить его годами таблетками — значит платить почками.

Стало:
Ибупрофен сужает сосуды внутри почки. Один раз — ничего страшного. Но если пить раз в неделю годами, **фильтрующая ткань замещается рубцовой** — той которая не восстанавливается. *Типичная история: человек приходит с давлением, уходит с диагнозом по почкам.* Боль — это сигнал. **Глушить его годами таблетками — значит платить почками.**

ПОСТ ДЛЯ ФОРМАТИРОВАНИЯ:
{post_text}

РЕЗУЛЬТАТ:
Только отформатированный пост. Без комментариев."""


def _format_examples(style_examples: list[str]) -> str:
    if not style_examples:
        return "Примеров нет — ориентируйся на эталон выше и задачу пользователя."
    return "\n\n".join(
        f"Пример {index}:\n{example}" for index, example in enumerate(style_examples, start=1)
    )
