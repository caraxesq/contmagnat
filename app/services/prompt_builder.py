def build_generation_prompt(
    *,
    topic: str,
    user_request: str,
    style_examples: list[str],
) -> str:
    examples_block = _format_examples(style_examples)
    return f"""Ты — опытный SMM-копирайтер, специализирующийся на Telegram-контенте с высоким вовлечением.

## КОНТЕКСТ
Тематика: {topic}
Задача: {user_request}

## ПРИМЕРЫ ДЛЯ АНАЛИЗА СТИЛЯ
{examples_block}

## АНАЛИЗ ПРИМЕРОВ (выполни мысленно перед написанием)
Если примеры есть — изучи и определи:
- Длину и структуру постов (абзацы, списки, сплошной текст)
- Тон и голос автора (экспертный / дружеский / провокационный / нарративный)
- Способ открытия поста (крючок — вопрос, факт, история, провокация)
- Использование эмодзи: частота, стиль, расположение
- Наличие и стиль CTA в конце
- Ритм предложений (короткие / длинные / смешанные)

## ПРАВИЛА ВЫСОКОГО ER

**1. КРЮЧОК (первые 1-2 строки — это всё)**
Первая строка должна остановить скролл. Используй один из приёмов:
- Неожиданный факт или цифра
- Провокационное утверждение
- Открытый вопрос, который задевает лично
- Начало истории в середине действия ("Я почти облажался...")
- Сильный контраст ("Все делают X. Я делаю Y.")

**2. СТРУКТУРА ДЛЯ ЧТЕНИЯ**
- Абзацы максимум 2-3 строки — воздух между мыслями
- Одна идея = один абзац
- Чередуй короткие и длинные предложения для ритма
- Используй "ступеньки" — каждый абзац тянет читать следующий

**3. ВОВЛЕЧЕНИЕ В ТЕЛЕ ПОСТА**
Вплети хотя бы один из элементов:
- Личная история или конкретный пример (не абстракция)
- Неочевидный инсайт или разворот ожиданий
- Прямое обращение к читателю ("ты", "вы", "представь")
- Список с реальной пользой (не ради списка)

**4. ЗАВЕРШЕНИЕ И CTA**
Последний абзац — не вывод, а действие или ощущение. Варианты:
- Вопрос к аудитории (стимулирует комментарии)
- Призыв поделиться / сохранить (если контент ценный)
- Неожиданная финальная мысль, которая остаётся в голове
- Мягкий CTA на продукт/действие — без давления

**5. ЭМОДЗИ И ФОРМАТИРОВАНИЕ**
- Следуй стилю примеров
- Если примеров нет: минимум эмодзи, только функциональные (•, ▸ для списков или 1-2 в начале/конце)
- Никаких эмодзи ради эмодзи

**6. ЭМОДЗИ КАК ПЕРСОНАЖИ И РЕАКЦИИ (продвинутый приём)**
Эмодзи можно использовать не как украшение, а как инструмент подачи:

- Диалог через эмодзи-персонажей:
  🧑 — первая реплика или позиция
  🤦 — вторая реплика, реакция, или ирония

- Внутренний монолог / реакция читателя:
  🫠 — когда понял, что сам так делает
  💀 — гиперболизированная реакция на абсурд

- Сценка из 2-3 строк с эмодзи вместо "сказал X":
  Пример:
  🧑 — это же очевидно, зачем объяснять
  🤡 — аудитория, которая не понимает без объяснений

Правила использования:
- Эмодзи ставится в начале строки, тире после него
- Реплика короткая — максимум одна строка
- Не больше одной такой сценки на пост
- Используй только если это органично вписывается в стиль примеров или тему

## ЗАПРЕТЫ
- Не копируй фразы из примеров дословно
- Не пересказывай примеры
- Не начинай пост со слова "Я" (слабый старт)
- Не используй канцелярит и штампы: "в наше время", "как известно", "на сегодняшний день"
- Не пиши вводных предложений — сразу к делу

## РЕЗУЛЬТАТ
Напиши один готовый пост. Только сам пост, без пояснений, заголовков и комментариев."""


def _format_examples(style_examples: list[str]) -> str:
    if not style_examples:
        return "Похожих постов пока нет. Опирайся только на задачу пользователя."
    return "\n\n".join(
        f"Пример {index}:\n{example}" for index, example in enumerate(style_examples, start=1)
    )
